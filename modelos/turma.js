class Turma{
    serie;
    nome;
    qtdAlunos;
    periodo;

    setSerie(serie){
        this.serie = serie;
    }
    getSerie(){
        return this.serie;
    }

    setNome(nome){
        this.nome = nome;
    }
    getNome(){
        return this.nome;
    }

    setQtdAlunos(qtdAlunos){
        this.qtdAlunos = qtdAlunos;
    }
    getQtdAlunos(){
        return this.qtdAlunos;
    }

    setPeriodo(periodo){
        this.periodo = periodo;
    }
    getPeriodo(){
        return this.periodo;
    }
}