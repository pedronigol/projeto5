class Estudante{
    nome; 
    idade; 
    email; 
    turma; 
        telefone;

    setNome(nome){
        this.nome = nome;
    }
    getNome(){
        return this.nome;
    }

    setIdade(idade){
        this.idade = idade;
    }
    getIdade(){
        return this.idade;
    }

    setEmail(email){
        this.email = email;
    }
    getEmail(){
        return this.email;
    }

    setTurma(turma){
        this.turma = turma;
    }
    getTurma(){
        return this.turma;
    }

    setTelefone(telefone){
        this.telefone = telefone;
    }
    getTelefone(){
        return this.telefone;
    }
}